#include <stdio.h>
#include <string.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n && (s1[i] != '\0' || s2[i] != '\0'))
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int	main(void)
{
	char			first[] = "maman";
	char			second[] = "mab";
	unsigned int	n;

	n = 4;
	printf("%d\n", ft_strncmp(first, second, n));
	printf("%d\n", strncmp(first, second, n));
}
