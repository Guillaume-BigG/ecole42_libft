#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int i;
	char *s;

	s = (char *)str;

	i = 0;
	while (s[i] != (char)c)
	{
		if (s[i] == '\0')
			return (NULL);
		i++;
	}
	return (s + i);
}